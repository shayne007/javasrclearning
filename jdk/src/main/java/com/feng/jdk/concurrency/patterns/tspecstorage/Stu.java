package com.feng.jdk.concurrency.patterns.tspecstorage;

public class Stu {
    private String name;
    private int age;
    public Stu() {}
    public Stu(String name, int age) {
        this.name = name;
        this.age = age;
    }
    public void setAge(int age) {
        this.age = age;
    }
} 